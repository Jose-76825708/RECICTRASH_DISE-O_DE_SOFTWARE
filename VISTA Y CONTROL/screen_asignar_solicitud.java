    private void jLabel9MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel9MouseClicked
    cConnection_1 con = new cConnection_1();

    try {
        int reporte_id = Integer.parseInt(jTextField1.getText());
        int reciclador_id = Integer.parseInt(jTextField2.getText());

        String sql = "{CALL dbo.sp_asignar_solicitudes(?, ?)}";

        try (Connection conn = con.obtenerConexion();
             CallableStatement cs = conn.prepareCall(sql)) {

            cs.setInt(1, reporte_id);
            cs.setInt(2, reciclador_id);

            cs.execute(); // Ejecuta el procedimiento

            JOptionPane.showMessageDialog(this,
                "Solicitud asignada correctamente al reciclador ID " + reciclador_id,
                "Asignación exitosa",
                JOptionPane.INFORMATION_MESSAGE);
        }

        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this,
                "Ingrese valores numéricos válidos para reporte y reciclador.",
                "Error de entrada",
                JOptionPane.ERROR_MESSAGE);
        } catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this,
                "Error al asignar la solicitud: " + ex.getMessage(),
                "Error SQL",
                JOptionPane.ERROR_MESSAGE);
        }
        
        
        
    }//GEN-LAST:event_jLabel9MouseClicked

